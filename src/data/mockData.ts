export function mockSearch(query: string, roles: string[], users: User[], verticals: string[] = []): User[] {
  const q = query.toLowerCase().trim();
  return users.filter((u) => {
    const matchesRoles = roles.length === 0 || u.roles.some((r) => roles.includes(r));
    if (!matchesRoles) return false;
    const matchesVerticals = verticals.length === 0 || (u.vertical != null && verticals.includes(u.vertical));
    if (!matchesVerticals) return false;
    if (!q) return true;
    return (
      `${u.firstName} ${u.lastName}`.toLowerCase().includes(q) ||
      u.currentRole.toLowerCase().includes(q) ||
      u.currentCompany.toLowerCase().includes(q) ||
      u.industry.toLowerCase().includes(q) ||
      u.roles.some((r) => r.includes(q))
    );
  });
}

